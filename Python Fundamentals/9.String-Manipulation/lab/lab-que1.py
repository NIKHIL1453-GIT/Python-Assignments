""" Write a Python program to demonstrate string slicing. """

str = "Nikhil Bhatia"

print(f"string from from index 1 to end using slicing:{str[1:]}")
print(f"Character from index value 2 to 9 using slicing:{str[2:9]}")
print(f"value between negative index -6 to -1 using slicing:{str[-6:-1]}")
print(f"Every alternate character of string using slicing:{str[::2]}")
print(f"Reversed string using slicing :{str[::-1]}")